package GenesteLussen;

public class Oefening3 {

    public static void main(String[] args) {
        int getal = 1;

        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 10; j++) {
                System.out.print(getal + "\t");
                getal++;
            }
            System.out.println();
        }
    }

}

