package Les1.OefeningenDeel2;

public class Berekening {

    public Berekening() {
    }

    public double Som(double getal1, double getal2){
        double som = getal1 + getal2;
        return som;
    }

    public double Vierkantswortel(double getal){
        double vierkantswortel = Math.sqrt(getal);
        return vierkantswortel;
    }
}
